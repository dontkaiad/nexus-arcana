"""core/list_classifier.py — regex pre-filters + Haiku fallback types for 🗒️ Списки."""
from __future__ import annotations

import re

# ── Покупки — создание ────────────────────────────────────────────────────────
# v1.2.1: «добавь в [группа]:» с двоеточием = маркер списка покупок
# (например «добавь в косметичка: тушь 800₽, крем 1500₽»). Negative lookahead
# отсекает системные группы (задачи / заметки / память / календарь / финансы /
# бюджет / инвентарь — у последнего свой regex). Группа: 2-40 символов из
# латиницы / кириллицы / цифр / `_-`.
_LIST_BUY_RE = re.compile(
    r"(?:"
    r"купи(?:ть)?\b"
    r"|надо\s+купить"
    r"|нужно\s+купить"
    r"|добавь\s+в\s+(?:"
    r"покупки|список"
    r"|(?!задач|заметк|памят|календар|финанс|бюджет|инвентар)"
    r"[A-Za-zА-Яа-я0-9_\-]{2,40}\s*:"
    r")"
    r"|в\s+список\b"
    r"|^добавь\s+товар"
    r")",
    re.IGNORECASE,
)

# ── Подзадачи — разбивка задачи на чеклист с привязкой ───────────────────────
_SUBTASK_RE = re.compile(
    r"разб(?:ей|ить)\s+(?:задачу|работу)\b|подзадачи\s+(?:для|к|у)\s+",
    re.IGNORECASE,
)

# ── Чеклист — создание ───────────────────────────────────────────────────────
_LIST_CHECK_RE = re.compile(
    r"список:\s|чеклист",
    re.IGNORECASE,
)

# ── Инвентарь — добавление (двоеточие обязательно: "дома есть: X") ────────────
# v8.x: расширено — занеси/закинь/положи/запиши/внеси в инвентарь (с категорией
# или без). Без префикса работает эвристика _looks_like_med_inventory() ниже.
_LIST_INV_ADD_RE = re.compile(
    r"дома есть:\s"
    r"|в\s+инвентар[ьеяи]"
    r"|(?:добавь|добавить|занеси|занести|закинь|закинуть|положи|положить"
    r"|запиши|записать|внеси|внести)\s+(?:в\s+)?инвентар[ьеяи]",
    re.IGNORECASE,
)

# ── Эвристика: «медицинский список без префикса» ────────────────────────────
# Многострочный текст где >=2 строк содержат фарм-маркер (мг/мл/шт/пачка/
# таблетки/капсулы/сироп/мазь/...) — это инвентарь, НЕ budget и не finance.
# Защищает от дорогостоящего ложного срабатывания Sonnet budget-анализа.
_PHARM_MARKER_RE = re.compile(
    r"\d+(?:[.,]\d+)?\s*"
    r"(?:мг|мл|г\b|шт\b|пачк|таблетк|капсул|ампул|сироп|табл\.|"
    r"свеч|капл|мазь|спрей|саше|драже|суппозитори)",
    re.IGNORECASE,
)


def _looks_like_med_inventory(text: str) -> bool:
    """True если text похож на список лекарств/расходников без явного префикса.

    Критерии:
    - >= 3 непустых строк
    - >= 2 строк содержат фарм-маркер (\\d+ + единица измерения)
    """
    if not text:
        return False
    lines = [l.strip() for l in text.splitlines() if l.strip()]
    if len(lines) < 3:
        return False
    matches = sum(1 for ln in lines if _PHARM_MARKER_RE.search(ln))
    return matches >= 2

# ── Инвентарь — поиск ("есть ибупрофен?", "есть ли дома X", "есть ли у меня X")
_LIST_INV_SEARCH_RE = re.compile(
    r"есть\s*(?:ли)?\s*(?:у меня)?\s*(?:дома)?\s*(?!задач|работ)\w+\??$",
    re.IGNORECASE,
)

# ── Чек покупки ("купила молоко 89р", "чек 4к привычки 1500") ────────────────
_LIST_DONE_RE = re.compile(
    r"купила?\b.*?\d+\s*[рр₽к]|^чек\s+",
    re.IGNORECASE,
)

# ── Инвентарь — обновление ("осталась 1 пачка", "закончился") ─────────────────
_LIST_INV_UPDATE_RE = re.compile(
    r"остал(?:ось?|ась?|ся)\s+\d|закончил(?:ся|ась|ось)|кончил(?:ся|ась)",
    re.IGNORECASE,
)

# ── v1.2: Команда суммы ("сумма Apple-стек", "сколько по продуктам") ──────────
_LIST_SUM_RE = re.compile(
    r"^\s*(?:сумма|сколько|итого|подсч[её]т)\s+(?:по\s+)?(.+?)\s*\??$",
    re.IGNORECASE,
)

